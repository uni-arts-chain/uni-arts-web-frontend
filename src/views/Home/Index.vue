import Carousel from "./Carousel";
import Top from "./Top";
import Thumbnail from "@/components/Thumbnail";
import ArtistGroupList from "@/components/ArtistGroupList";
export default {
    name: "index",
    components: { Carousel, Thumbnail, ArtistGroupList, Top },
    data() {
        return {
            workList: [],
            recommendList: [],
            authorList: [],
        };
    },
    created() {
        this.requestAuthorData();
        this.requestWorkData();
        this.requestRecommendData();
    },
    mounted() {},
    methods: {
        requestAuthorData() {
            this.$http
                .globalGetPopularAuthor({})
                .then((res) => {
                    this.authorList = res;
                })
                .catch((err) => {
                    console.log(err);
                    this.$notify.error(err.head ? err.head.msg : err);
                });
        },
        requestWorkData() {
            this.$http
                .globalGetPopArts({})
                .then((res) => {
                    this.workList = res;
                })
                .catch((err) => {
                    console.log(err);
                    this.$notify.error(err.head ? err.head.msg : err);
                });
        },
        requestRecommendData() {
            this.$http
                .globalGetTopicArts({})
                .then((res) => {
                    this.recommendList = res;
                })
                .catch((err) => {
                    console.log(err);
                    this.$notify.error(err.head ? err.head.msg : err);
                });
        },
    },
};
</script>

